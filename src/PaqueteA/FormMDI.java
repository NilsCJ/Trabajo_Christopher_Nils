    private void mnuArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuArchivoActionPerformed
        // TODO add your handling code here:

        //primero hacemos una instancia del sub formulario
        //En este caso el formulario que se llamara
        
        FrmCliente cliente = new FrmCliente();
        
        //Agregar la instancia la mida al contenedor
        
        mdi.add(cliente);
        
        //centrar form
        
        //Tama単o del contenedor
        Dimension sizeContenedor = mdi.getSize();
        
        //Tama単o del formulario interno
        Dimension sizeEjemplo = cliente.getSize();
        
        cliente.setLocation( (((int)sizeContenedor.getWidth()-
                              (int)sizeEjemplo.getWidth())/2),
                              ((int)sizeContenedor.getHeight()- 
                              (int)sizeEjemplo.getHeight())/2);
        
        cliente.show();

    }//GEN-LAST:event_mnuArchivoActionPerformed

